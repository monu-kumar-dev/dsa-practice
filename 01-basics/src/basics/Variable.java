package basics;

public class Variable {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x);
        int y;
        y = 20;
        System.out.println(y);
        System.out.println(x * y);

        int a;
        a = 25;
        System.out.println(a);
        a = a - 5;
        System.out.println(a);
        a = a * 10;
        System.out.println(a);

    }
}
